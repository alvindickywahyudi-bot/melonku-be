<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Factories\HasFactory;

class OrderItem extends Model
{
    use HasFactory;

    /*
    |--------------------------------------------------------------------------
    | 🧱 TABLE
    |--------------------------------------------------------------------------
    */
    protected $table = 'order_items';

    /*
    |--------------------------------------------------------------------------
    | 🛡️ FILLABLE
    |--------------------------------------------------------------------------
    */
    protected $fillable = [

        /*
        |--------------------------------------------------------------------------
        | RELATION
        |--------------------------------------------------------------------------
        */
        'order_id',

        'product_id',

        /*
        |--------------------------------------------------------------------------
        | PRODUCT SNAPSHOT
        |--------------------------------------------------------------------------
        */
        'product_name',

        'product_image',

        /*
        |--------------------------------------------------------------------------
        | ORDER DATA
        |--------------------------------------------------------------------------
        */
        'qty',
        
        'price',
        
        'produk_varian_id',
        
        'selected_weight',
    ];

    /*
    |--------------------------------------------------------------------------
    | 🔄 CASTS
    |--------------------------------------------------------------------------
    */
protected $casts = [

    'qty' => 'integer',

    'price' => 'integer',

    'selected_weight' => 'integer',
];

    /*
    |--------------------------------------------------------------------------
    | 🔗 RELATIONS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | ORDER
    |--------------------------------------------------------------------------
    */
    public function order()
    {
        return $this->belongsTo(
            Order::class
        );
    }

    /*
    |--------------------------------------------------------------------------
    | PRODUCT
    |--------------------------------------------------------------------------
    */
    public function produk()
    {
        return $this->belongsTo(
            Produk::class,
            'product_id'
        );
    }

/*
|--------------------------------------------------------------------------
| VARIAN
|--------------------------------------------------------------------------
*/
public function varian()
{
    return $this->belongsTo(
        ProdukVarian::class,
        'produk_varian_id'
    );
}

    /*
    |--------------------------------------------------------------------------
    | 🧠 HELPERS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | SUBTOTAL
    |--------------------------------------------------------------------------
    */
public function subtotal()
{
    return (
        (int) $this->qty *
        (int) $this->price
    );
}

    /*
    |--------------------------------------------------------------------------
    | MONEY FORMAT
    |--------------------------------------------------------------------------
    */
    public function formattedPrice()
    {
        return number_format(
            $this->price,
            0,
            ',',
            '.'
        );
    }

    public function formattedSubtotal()
    {
        return number_format(
            $this->subtotal(),
            0,
            ',',
            '.'
        );
    }
    
    public function formattedWeight()
    {
        return number_format(
            ((int) $this->selected_weight) / 1000,
            1
        ) . ' Kg';
    }
    
}